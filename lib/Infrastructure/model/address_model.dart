class Address {
  String name;
  String phoneNumber;
  String pinCode;
  String city;
  String state;
  String houseNo;
  String area;
  Address({
    required this.name,
    required this.phoneNumber,
    required this.pinCode,
    required this.city,
    required this.state,
    required this.area,
    required this.houseNo,
  });
}
